/**
 * SideNavigationItem interaction modes.
 * @public
 */
enum SideNavigationItemInteractionMode {
	/**
	 * Item is selectable.
	 * @public
	 */
	Select = "Select",

	/**
	 * Only leaves are selectable.
	 * @public
	 */
	SelectLeavesOnly = "SelectLeavesOnly",
}

export default SideNavigationItemInteractionMode;
