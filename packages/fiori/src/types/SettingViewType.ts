/**
 * Different SettingView types.
 * @public
 */
enum SettingViewType {
	/**
	 * The default view type. If specified, the view will be created as a tab.
	 * @public
	 */
	Tab = "Tab",

	/**
	 * The page view type. If specified, the view will be created as a page.
	 * @public
	 */
	Page = "Page",
}

export default SettingViewType;
