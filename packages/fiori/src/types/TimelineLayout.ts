/**
 * Available Timeline layout orientation
 *
 * @class
 * @enum {string}
 * @public
 * @author SAP SE
 * @alias sap.ui.webc.fiori.types.TimelineLayout
 */
enum TimelineLayout {
	/**
	 * Vertical layout
	 * Default type
	 * @public
	 * @type {Vertical}
	 */
	Vertical= "Vertical",

	/**
	 * Horizontal layout
	 * @public
	 * @type {Horizontal}
	 */
	Horizontal= "Horizontal",
}

export default TimelineLayout;
