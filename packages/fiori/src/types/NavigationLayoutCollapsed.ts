/**
 * Specifies whether the navigation layout is in collapsed or expanded mode.
 * @public
 */
enum NavigationLayoutCollapsed {
	/**
	 * Auto
	 * @public
	 */
	Auto = "Auto",
	/**
	 * Collapsed
	 * @public
	 */
	Collapsed = "Collapsed",
	/**
	 * Expanded
	 * @public
	 */
	Expanded = "Expanded",
}

export default NavigationLayoutCollapsed;
