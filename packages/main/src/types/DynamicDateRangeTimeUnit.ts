/**
 * Different time units for dynamic date range calculations.
 * @public
 */
enum DynamicDateRangeTimeUnit {
	/**
	 * Time unit in days.
	 * @public
	 */
	Days = "days",

	/**
	 * Time unit in weeks.
	 * @public
	 */
	Weeks = "weeks",

	/**
	 * Time unit in months.
	 * @public
	 */
	Months = "months",

	/**
	 * Time unit in quarters.
	 * @public
	 */
	Quarters = "quarters",

	/**
	 * Time unit in years.
	 * @public
	 */
	Years = "years",
}

export default DynamicDateRangeTimeUnit;
