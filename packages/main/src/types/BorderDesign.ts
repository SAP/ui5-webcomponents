/**
 * Defines border designs.
 *
 * @readonly
 * @enum {string}
 * @public
 * @author SAP SE
 * @alias sap.ui.webc.main.types.BorderDesign
 */
enum BorderDesign {
	/**
	 * A solid border color dependent on the theme.
	 * @public
	 * @type {Solid}
	 */
	Solid = "Solid",

	/**
	 * Specifies no border.
	 * @public
	 * @type {Transparent}
	 */
	None = "None",
}

export default BorderDesign;
