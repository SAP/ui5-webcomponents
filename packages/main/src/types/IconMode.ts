/**
 * Different Icon modes.
 * @public
 */
enum IconMode {
	/**
	 * Image mode (by default)
	 * @public
	 */
	Image = "Image",

	/**
	 * Decorative mode
	 * @public
	 */
	Decorative = "Decorative",

	/**
	 * Interactive mode
	 * @public
	*/
	Interactive = "Interactive",
}

export default IconMode;
