/**
 * Menu item selection modes.
 * @since 2.5.0
 * @public
 */
enum ItemSelectionMode {
	/**
	 * default type (no item selection)
	 * @public
	 */
	None = "None",

	/**
	 * Single item selection mode
	 * @public
	 */
	Single = "Single",

	/**
	 * Multiple items selection mode
	 * @public
	 */
	Multiple = "Multiple",
}

export default ItemSelectionMode;
