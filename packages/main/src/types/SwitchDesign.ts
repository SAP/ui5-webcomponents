/**
 * Different types of Switch designs.
 *
 * @class
 * @enum {string}
 * @public
 * @author SAP SE
 * @alias sap.ui.webc.main.types.SwitchDesign
 */
enum SwitchDesign {
	/**
	 * Defines the Switch as Textual
	 * @public
	 * @type {Textual}
	 */
	Textual = "Textual",

	/**
	 * Defines the Switch as Graphical
	 * @public
	 * @type {Graphical}
	 */
	Graphical = "Graphical",
}

export default SwitchDesign;
