/**
 * Defines the content visualisation of ui5-search popup.
 * @public
 */
 enum SearchPopupMode {
	/**
	 * Defines the content visualisation of ui5-search popup as illustrated message.
	 * @public
	 */
	Illustration = "Illustration",

	/**
	 * Defines the content visualisation of ui5-search popup as list of items.
	 * @public
	 */
	List = "List",

	/**
	 * Defines the content visualisation of ui5-search popup as loading spinner.
	 * @public
	 */
	Loading = "Loading",
}

export default SearchPopupMode;
