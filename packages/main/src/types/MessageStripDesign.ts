/**
 * MessageStrip designs.
 * @public
 */
enum MessageStripDesign {
	/**
	 * Message should be just an information
	 * @public
	 */
	Information = "Information",

	/**
	 * Message is a success message
	 * @public
	 */
	Positive = "Positive",

	/**
	 * Message is an error
	 * @public
	 */
	Negative = "Negative",

	/**
	 * Message is a warning
	 * @public
	 */
	Warning = "Warning",
}

export default MessageStripDesign;
