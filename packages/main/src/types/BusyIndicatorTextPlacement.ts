/**
 * Different BusyIndicator text placement.
 *
 * @public
 */
enum BusyIndicatorTextPlacement {
	/**
	 * Text will be on top of busy indicator
	 * @public
	 */
	Top = "Top",

	/**
	 * Text will be on bottom of busy indicator
	 * @public
	 */
	Bottom = "Bottom",
}

export default BusyIndicatorTextPlacement;
