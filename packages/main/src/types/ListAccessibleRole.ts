/**
 * List accessible roles.
 * @public
 */
enum ListAccessibleRole {

	/**
	 * Represents the ARIA role "list". (by default)
	 * @public
	 */
	List = "List",

	/**
	 * Represents the ARIA role "menu".
	 * @public
	 */
	Menu = "Menu",

	/**
	 * Represents the ARIA role "tree".
	 * @public
	 */
	Tree = "Tree"

}

export default ListAccessibleRole;
