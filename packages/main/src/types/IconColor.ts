/**
 * Different Icon semantic colors.
*
* @class
* @enum {string}
* @public
* @author SAP SE
* @alias sap.ui.webc.main.types.IconColor
*/
enum IconColor {
	/**
	 * Contrast color
	 * @public
	 * @type {Contrast}
	 */
	Contrast = "Contrast",

	/**
	 * Critical color
	 * @public
	 * @type {Critical}
	 */
	Critical = "Critical",

	/**
	 * Default color (brand color)
	 * @public
	 * @type {Default}
	*/
	Default = "Default",

	/**
	 * info type
	 * @public
	 * @type {Information}
	 */
	Information = "Information",

	/**
	 * Color for icon used as a marker
	 * @public
	 * @type {Marker}
	 */
	Marker = "Marker",

	/**
	 * Negative color
	 * @public
	 * @type {Negative}
	 */
	Negative = "Negative",

	/**
	 * Neutral color
	 * @public
	 * @type {Neutral}
	 */
	Neutral = "Neutral",

	/**
	 * Color that indicates an icon which isn't interactive
	 * @public
	 * @type {NonInteractive}
	 */
	NonInteractive = "NonInteractive",

	/**
	 * Positive color
	 * @public
	 * @type {Positive}
	 */
	Positive = "Positive",

	/**
	 * Color for icon used in a Tile
	 * @public
	 * @type {Tile}
	 */
	Tile = "Tile"
}

export default IconColor;
