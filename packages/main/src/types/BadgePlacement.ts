/**
 * Determines where the badge is placed.
 * @public
 */
enum BadgePlacement {
	/**
	 * The button does not show badge.
	 * @public
	 */
	None = "None",
	/**
	 * The button renders the badge at the end.
	 * @public
	 */
	End = "End",

	/**
	 * The button renders the badge at the top end corner.
	 * @public
	 */
	EndTop = "EndTop",
}

export default BadgePlacement;
