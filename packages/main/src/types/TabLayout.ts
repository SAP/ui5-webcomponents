/**
 * Tab layout of TabContainer.
 *
 * @public
 */
enum TabLayout {
	/**
	 * Inline type, the tab "main text" and "additionalText" are displayed horizotally.
	 * @public
	 * @type {Inline}
	 */
	Inline = "Inline",

	/**
	 * Standard type, the tab "main text" and "additionalText" are displayed vertically.
	 * @public
	 * @type {Standard}
	 */
	Standard = "Standard",
}

export default TabLayout;
