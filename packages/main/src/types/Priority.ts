/**
 * Different types of Priority.
 *
 * @enum {string}
 * @public
 */
enum Priority {
	/**
	 * High priority.
	 * @public
	 * @type {High}
	 */
	High = "High",

	/**
	 * Medium priority.
	 * @public
	 * @type {Medium}
	 */
	Medium = "Medium",

	/**
	 * Low priority.
	 * @public
	 * @type {Low}
	 */
	Low = "Low",

	/**
	 * Default, none priority.
	 * @public
	 * @type {None}
	 */
	None = "None",
}

export default Priority;
