/**
 * Different types of HasPopup.
 * @public
 */
enum HasPopup {
	/**
	 * Dialog popup type.
	 * @public
	 */
	Dialog = "Dialog",

	/**
	 * Grid popup type.
	 * @public
	 */
	Grid = "Grid",

	/**
	 * ListBox popup type.
	 * @public
	 */
	ListBox = "ListBox",

	 /**
	  * Menu popup type.
	  * @public
	  */
	Menu = "Menu",

	 /**
	  * Tree popup type.
	  * @public
	  */
	Tree = "Tree",
}

export default HasPopup;
