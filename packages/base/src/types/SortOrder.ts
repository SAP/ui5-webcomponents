/**
 * Defines the sort order.
 *
 * @public
 * @since 2.8.0
 */
enum SortOrder {
	/**
	 * Sorting is not applied.
	 * @public
	 */
	None = "None",

	/**
	 * Sorting is applied in ascending order.
	 * @public
	 */
	Ascending = "Ascending",

	/**
	 * Sorting is applied in descending order.
	 * @public
	 */
	Descending = "Descending",
}

export default SortOrder;
