/**
 * Different navigation modes for ItemNavigation.
 *
 * @class
 * @enum {string}
 * @public
 * @author SAP SE
 * @alias sap.ui.webc.base.types.NavigationMode
 */
enum NavigationMode {
	/**
	 * @public
	 * @type {Auto}
	 */
	Auto = "Auto",
	/**
	 * @public
	 * @type {Vertical}
	 */
	Vertical = "Vertical",
	/**
	 * @public
	 * @type {Horizontal}
	 */
	Horizontal = "Horizontal",
	/**
	 * @public
	 * @type {Paging}
	 */
	Paging = "Paging",
}

export default NavigationMode;
