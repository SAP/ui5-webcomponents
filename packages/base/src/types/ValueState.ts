/**
 * Different types of ValueStates.
 *
 * @public
 */
enum ValueState {
	/**
	 *
	 * @public
	 * @type {None}
	 */
	None = "None",

	/**
	 *
	 * @public
	 * @type {Success}
	 */
	Success = "Success",

	/**
	 *
	 * @public
	 * @type {Warning}
	 */
	Warning = "Warning",

	/**
	 *
	 * @public
	 * @type {Error}
	 */
	Error = "Error",

	/**
	 *
	 * @public
	 * @type {Information}
	 */
	Information = "Information",
}

export default ValueState;
