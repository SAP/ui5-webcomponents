/**
 * Types of ARIA roles.
 * @public
 */
enum AriaRole {
	/**
	* The ARIA role "alertdialog".
	* @public
	*/
	AlertDialog = "AlertDialog",

	/**
	 * The ARIA role "button".
	 * @public
	 */
	Button = "Button",

	/**
	 * The ARIA role "dialog".
	 * @public
	 */
	Dialog = "Dialog",

	/**
	 * The ARIA role "link".
	 * @public
	 */
	Link = "Link",
}

export default AriaRole;
